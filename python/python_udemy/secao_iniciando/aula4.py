# Operadores Aritméticos

soma = 1 + 2
print('soma', soma)

subtracao = 2 - 1
print('subtracao', subtracao)

divisao = 100 / 10
print('divisao', divisao)

divisao_inteira = 10 // 3
print('divisao_inteira', divisao_inteira)

modulo = 55 % 2
print('modulo', modulo)

multiplicacao = 5 * 5
print('multiplicacao', multiplicacao)

exponenciacao = 5 ** 5
print('exponenciacao', exponenciacao)

# Concatenação - Usar o sinal de + para unir strings
print('a' + 'b' + 'c')

# Multiplicação de strint
print(3 * '*Bruno*\n')
